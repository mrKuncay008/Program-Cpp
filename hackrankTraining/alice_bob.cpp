vector<int> compareTriplets(vector<int> a, vector<int> b) {
    int aAlice = (a[0] > b[0]) + (a[1] > b[1]) + (a[2] > b[2]);
    int bBob = (a[0] < b[0]) + (a[1] < b[1]) + (a[2] < b[2]);

    return {aAlice, bBob};

}

